nome = "Caixa D'água"
print(nome)
ironia = "Você é \"rico\""
-- Usamos a barra invertida (caractere de escape) para imprimir strings
-- especiais
print(ironia)

barra = "Texto com \\"
print(barra)
-- O caractere \n é usado para criar uma nova linha
variasLinhas = "Linha 1\nLinha 2"
print(variasLinhas)
-- O /t cria um tab (parágrafo, tabulação)
print("\tEspaço antes")
-- O /r retorna a string para sua posição inicial
print("\tEu tenho tab!\tEu também!\rEu gostaria de uma tab, mas fui retornado!")
print("\t\rEu retornei, mas há uma tabulação acima de mim!")
