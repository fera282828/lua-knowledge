print("Soma de 2 + 1:")
print(1 + 1)
print("Subtração de 2 - 1:")
print(2 - 1)
--[[ Podemos inserir as operações diretamente na função print(), e ela retorna
o resultado --]]

print("3 * 3:") -- O sinal de multiplicação é o asterisco (*)
print(3 * 3)

print("4 / 2:") -- No caso da divisão, é a barra (/)
print(4 / 2)

--[[ Na computação, temos a operação módulo, que retorna o resto
de uma divisão. É representada pelo sinal de porcentagem (%) --]]

print("9 % 3:")
print(9 % 3)

print("9 % 2:")
print(9 % 2)
