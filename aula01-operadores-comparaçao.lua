-- Operadores de comparação: >, <, >= e <=
print("2 > 3?")
print(2 > 3)
-- Operadores de comparação retornam um valor do tipo booleano (1 ou 0)
print("2 < 3?")
print(2 < 3)

print("2 <= 3?")
print(2 <= 3)

print("2 >= 3?")
print(2 >= 3)

print("532 > 3?")
print(532 > 3)
